void MD5_Hash(const unsigned char* data, int len,unsigned char hash[16])
{
	MD5_CTX ctx;
	MD5_Init(&ctx);
	MD5_Update(&ctx,data,(unsigned)len);
	MD5_Final(&ctx);
	for(int i=0;i<16;++i)
		hash[i]=ctx.digest[i];
}




